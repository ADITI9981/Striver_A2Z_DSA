/*Given a positive integer n, The task is to find the value of Î£i F(i) where i is from 1 to n and function F(i) is defined as the sum of all divisors of i.

Examples:

Input: n = 4
Output: 15
Explanation:
F(1) = 1
F(2) = 1 + 2 = 3
F(3) = 1 + 3 = 4
F(4) = 1 + 2 + 4 = 7
So, F(1) + F(2) + F(3) + F(4)
    = 1 + 3 + 4 + 7 = 15*/
#include <bits/stdc++.h>
using namespace std;

class Solution {
  public:
    int sumOfDivisors(int n) {
        int sum=0;
        for(int i=1;i<=n;i++){
            if (i == 0) {
            return -1;  
            }
            sum +=i*(n/i);
        }
        return sum;
    }
};


int main() {
    int t;
    cin >> t;
    while (t--) {
        int N;
        cin >> N;
        Solution ob;
        int ans = ob.sumOfDivisors(N);
        cout << ans << endl;

        cout << "~"
             << "\n";
    }
    return 0;
}
